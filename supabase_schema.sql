-- ───────────────────────────────────────────────────────
-- FUNCTION: get_sevas_near (PostGIS proximity search)
-- ───────────────────────────────────────────────────────
CREATE OR REPLACE FUNCTION get_sevas_near(
  lat float8, lng float8, radius_km float8 DEFAULT 10
)
RETURNS SETOF public.sevas AS $$
  SELECT * FROM public.sevas
  WHERE is_live = true
    AND ST_DWithin(
      geo_point,
      ST_MakePoint(lng, lat)::geography,
      radius_km * 1000
    )
  ORDER BY geo_point <-> ST_MakePoint(lng, lat)::geography;
$$ LANGUAGE sql STABLE;

