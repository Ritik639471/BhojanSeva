-- Enable PostGIS extension (for spatial queries)
CREATE EXTENSION IF NOT EXISTS postgis;

-- ───────────────────────────────────────────────────────
-- TABLE: sevas (main listings)
-- ───────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.sevas (
  id            bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id       uuid REFERENCES auth.users(id) ON DELETE SET NULL,
  title         text NOT NULL,
  food          text NOT NULL,
  type          text NOT NULL,
  permanence    text DEFAULT 'temporary',
  is_live       boolean DEFAULT true,
  status        text DEFAULT 'serving',
  is_sos        boolean DEFAULT false,
  tags          text[] DEFAULT '{}'::text[],
  allergens     text[] DEFAULT '{}'::text[],
  finished_votes bigint[] DEFAULT '{}'::bigint[],
  location      jsonb NOT NULL,
  geo_point     geography(Point, 4326),   -- PostGIS spatial column
  distance      text,
  time          text,
  start_time    timestamptz,
  end_time      timestamptz,
  estimated_servings integer DEFAULT 0,
  servings_left  integer DEFAULT 0,
  going_count   integer DEFAULT 0,
  trust_score   integer DEFAULT 100,
  is_moving     boolean DEFAULT false,
  is_verified   boolean DEFAULT false,
  is_featured   boolean DEFAULT false,
  photo_url     text,
  photo_urls    text[] DEFAULT '{}'::text[],
  sponsor_name  text,
  sponsor_amount integer DEFAULT 0,
  city          text DEFAULT 'Delhi',
  contribution_score integer DEFAULT 0,
  report_count  integer DEFAULT 0,
  ai_description text,
  created_at    timestamptz DEFAULT timezone('utc', now()) NOT NULL,
  updated_at    timestamptz DEFAULT timezone('utc', now())
);

-- Spatial index for fast proximity queries
CREATE INDEX IF NOT EXISTS idx_sevas_geo ON public.sevas USING GIST (geo_point);
CREATE INDEX IF NOT EXISTS idx_sevas_city ON public.sevas (city);
CREATE INDEX IF NOT EXISTS idx_sevas_is_live ON public.sevas (is_live);
CREATE INDEX IF NOT EXISTS idx_sevas_user_id ON public.sevas (user_id);

-- Auto-update updated_at trigger
CREATE OR REPLACE FUNCTION update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN NEW.updated_at = NOW(); RETURN NEW; END;
$$ language 'plpgsql';

DROP TRIGGER IF EXISTS update_sevas_updated_at ON public.sevas;
CREATE TRIGGER update_sevas_updated_at
  BEFORE UPDATE ON public.sevas
  FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();

-- ───────────────────────────────────────────────────────
-- TABLE: seva_votes (crowd status votes)
-- ───────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.seva_votes (
  id         bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  seva_id    bigint REFERENCES public.sevas(id) ON DELETE CASCADE,
  voter_fingerprint text,   -- browser fingerprint (no login required)
  vote_type  text NOT NULL CHECK (vote_type IN ('serving','low','finished','trust_up','trust_down')),
  created_at timestamptz DEFAULT timezone('utc', now()) NOT NULL
);

CREATE INDEX IF NOT EXISTS idx_votes_seva_id ON public.seva_votes (seva_id);
CREATE INDEX IF NOT EXISTS idx_votes_created ON public.seva_votes (created_at);

-- ───────────────────────────────────────────────────────
-- TABLE: going_tracking (I'm Going)
-- ───────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.going_tracking (
  id         bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  seva_id    bigint REFERENCES public.sevas(id) ON DELETE CASCADE,
  user_fingerprint text NOT NULL,
  created_at timestamptz DEFAULT timezone('utc', now()) NOT NULL,
  UNIQUE(seva_id, user_fingerprint)
);

-- ───────────────────────────────────────────────────────
-- TABLE: saved_sevas (bookmarks)
-- ───────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.saved_sevas (
  id      bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id uuid REFERENCES auth.users(id) ON DELETE CASCADE,
  seva_id bigint REFERENCES public.sevas(id) ON DELETE CASCADE,
  created_at timestamptz DEFAULT timezone('utc', now()) NOT NULL,
  UNIQUE(user_id, seva_id)
);

-- ───────────────────────────────────────────────────────
-- TABLE: audit_log (moderation trail)
-- ───────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.audit_log (
  id         bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  seva_id    bigint REFERENCES public.sevas(id) ON DELETE CASCADE,
  user_id    uuid REFERENCES auth.users(id) ON DELETE SET NULL,
  action     text NOT NULL,
  old_value  jsonb,
  new_value  jsonb,
  created_at timestamptz DEFAULT timezone('utc', now()) NOT NULL
);

-- ───────────────────────────────────────────────────────
-- TABLE: reports (anonymous flagging)
-- ───────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.reports (
  id         bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  seva_id    bigint REFERENCES public.sevas(id) ON DELETE CASCADE,
  reason     text NOT NULL,
  details    text,
  reporter_fingerprint text,
  created_at timestamptz DEFAULT timezone('utc', now()) NOT NULL
);

-- ───────────────────────────────────────────────────────
-- TABLE: user_profiles (contribution scores)
-- ───────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.user_profiles (
  id               uuid PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
  display_name     text,
  avatar_url       text,
  city             text DEFAULT 'Delhi',
  contribution_score integer DEFAULT 0,
  sevas_added      integer DEFAULT 0,
  sevas_verified   integer DEFAULT 0,
  is_verified_organizer boolean DEFAULT false,
  is_ngo           boolean DEFAULT false,
  bio              text,
  created_at       timestamptz DEFAULT timezone('utc', now()) NOT NULL
);

